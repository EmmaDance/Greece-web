			<li>Fiul regelui Macedoniei, Filip al doilea (359-336 î.Hr)</li>
			<br>
			</ul>
			<p>Dotat cu spirit de aventura, cu o deosebită inteligență și excelente calități de comandant militar, cu o energie ieșită din comun, elevul lui Aristotel, a creat primul imperiu universal din istorie și a uimit prin dezchiderea pe care a dovedit-o realizarea unei originale sinteze dintre lumea greacă și Orientul cucerit.</p>
			<p>Toate proiectele le punea în practică uluitor de repede. După ce și-a împărțit averea prietenilor și a reprimat p aristocrații din armata macedoneană care se opuseseră reformelor inițiate de tatăl său, a trecut la politica de cuceriri. A început cu Tracia(a făcut și o demonstrație de forță, de o zi, la nord de Dunăre, în ținutul geto-dacilor), au urmat apoi orașele din Grecia continentală, trecerea Hellespontului și marile cuceriri din Orientul Antic. Succesele expedițiilor militare au fost datorate atât tacticilor de luptă, adecvate după situație - <a href="images/falanga.jpg">falanga macedoneană</a> dovedindu-se foarte eficientă - cât și unor reforme de ordin militar, cum a fost crearea unităților de luptă combinate. </p>
			<p>Alexandru Macedon a fost și un mare diplomat. În teritoriile cucerite a adoptat atitudinea suveranilor locali, legitimându-și succesiunea la tronul acestora. Pentru regele persan învins, Darius al III-lea, a organizat funeralii imperiale, ucigașul regelui, satrapul Bessos al Bactriei, fiind executat, iar regelui Poros, deși învins, i-a lăsat regatul. În 324, cu ocaia căsătoriei sale cu Sateira, una dintre fiicele lui Darius al III-lea, la Susa, s-au căsătorit 10,000 de macedoneni cu fete persane. Nu totdeauna a luat deciziile cele mai așteptate; a fost necruțător cu populația unor orașe învinse(locuitorii Tebei au fst vânduți ca sclavi, toți bărbații din Tyr au fost uciși, iar femeile și copiii, vânduți ca sclavi), a distrus unele importante centre urbane(Teba, Persepolis), a ucis pe unii generali capabili și apropiați lui. </p>
			<p>Alexandru Macedon, trecut în rândul zeilor de preoții din Egipt, a murit la 13 iunie 323 î.Hr la Babilon, pe drumul de întoarcere din marea aventură a cuceririi Orientului.</p>
			<p>Imperiul s-a destrămat tot așa de repede pe cât s-a creat. Dar Alexandru a lăsat în urmă o epocă inaugurată de el. Epoca elenistică.</p>
