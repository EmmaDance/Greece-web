					<p>În 461 î.Hr, Pericle a ajuns conducător al partidului democrat, opus partidului oligarhilor, al cărui șef, Cimon, fiul lui Mirtiade, a fost ostracizat din inițiativa sa. În 454 î.Hr, a adus tezaurul comun al grecilor de la Delos la Atena</p>
					<p>Timp de 15 ani consecutiv, 443-429 î.Hr, a îndeplinit funcția de prim-strateg, situație unica la atenieni. Guvernarea lui a fost considerată "Secolul de aur" al cetății Atena. În timpul său au fost construite ansamblul arhitectural de pe Acropolă și zidurile care legau Atena de portul Pireu. În casa lui Pericle a fot primul salon literar a grecilor, sub patronajul soției sale, miletana Aspasia, o femeie deosebit de cultă. </p>
					<p>Pericle a fost prieten cu cele mai strălucite personalități din vremea lui: filosofii Xenon și Anaxagoras, dramaturgii Sofocle, Eschil, Euripide și Aristofan, istoricul Herodot, sculptorii Phidias și Miron, pictorii Asion și Polignot, arhitecții Hippodamos, Callicrate, Coroibos și Ictinos.</p>
